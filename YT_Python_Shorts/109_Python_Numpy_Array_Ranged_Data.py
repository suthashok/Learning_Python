# Ranged Data in Numpy Python

import numpy as np

# arange Function [m,n,(s))
# Upcast similar to Numpy Array


#Ex. 1 Create a Numpy array with numbers from 0 to 10
array1=np.arange(0,11)
print(array1)

#Ex. 2 Create Numpy array with numbers from 0 to 10 with increment of 2
array2=np.arange(0,11,2)
print(array2)

#Ex. 3 Create Numpy array of 4 elements with numbers from 0 to 10


# linspace function [a,b,(n)]
array3=np.linspace(0,10,4)
print(array3)
